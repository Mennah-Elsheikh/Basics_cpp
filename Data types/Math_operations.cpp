
using namespace std;

#include <iostream>

int main()
{
    double x, y;
    cin >> x >> y;
    cout << x << " + " << y << " = " << x + y << endl;
    cout << x << " - " << y << " = " << x - y << endl;
    cout << x << " / " << y << " = " << x / y << endl;
    cout << x << " * " << y << " = " << x * y << endl;
}
